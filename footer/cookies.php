  <h1>Politique en matière de cookies</h1>
  <br>
  <p>Cette politique en matière de cookies expose comment Pizzashop ("nous") utilise les cookies (ou autres traceurs).</p>
  <br>
  <p>Le terme de "cookies" est souvent utilisé pour décrire différents types de technologies, telles que les cookies HTML, les cookies Flash, les scripts, les pixels ou les balises. Chacune de ces technologies repose sur le dépôt d'un petit fichier texte placé sur un appareil visiteur par un serveur Web, qui pourra être lu lors d'une utilisation ultérieure. Pour des raisons pratiques, l'ensemble de ces technologies est ci-après appelé collectivement "cookies". Les cookies sont utilisés, entre autres, pour assurer la sécurité du site Web, pour sauvegarder les paramètres, pour se connecter au site Web, pour analyser la façon dont le site Web est utilisé et pour adapter le contenu du site Web à l'utilisateur.</p>
  <br>
  <h2>Les types de cookies que nous utilisons</h2>
  <br><br>
  <h3>Cookies strictement nécessaires</h3>
  <br>
  <p>Ces cookies sont essentiels afin de vous permettre de naviguer sur le site Web et d'utiliser ses fonctionnalités telles que l'accès à des parties sécurisées ou payantes du site Web. Sans ces cookies, les services que vous avez requis, tels que la connexion "sans mot de passe" ou le panier d'achats ne peuvent pas être fournis. Ces cookies étant essentiels à l'utilisation d'un site internet ils ne peuvent pas être désactivés sans affecter considérablement votre expérience utilisateur.</p>
  <br><br>
  <h3>Cookies de mesure d'audience</h3>
  <br>
  <p>Nous utilisons les cookies Google Analytics afin de nous aider à comprendre comment les clients arrivent ou naviguent sur notre site, comment ils l'utilisent mais également afin de nous aider à identifier les domaines dans lesquels nous pouvons améliorer notre site internet tels que la navigation, l'expérience d'achat et les campagnes marketing.</p>
  <br><br>
  <h3>Cookies de performance</h3>
  <br>
  <p>Ces cookies recueillent des informations sur la façon dont vous utilisez notre site Web et nous aident à améliorer le fonctionnement et les performances de celui-ci. Les données stockées par ces cookies ne contiennent jamais d'informations personnelles permettant d'en déduire votre identité.</p>
  <br><br>
  <h3>Cookies de personnalisation</h3>
  <br>
  <p>Ces cookies de personnalisation se souviennent de vos informations de connexion et de vos préférences afin de vous éviter de taper votre nom d'utilisateur à chaque fois que vous accédez au site, et afin de se souvenir de vos préférences linguistiques. Ils sont utilisés pour vous fournir une expérience conforme à vos préférences et pour vous rendre les visites plus adaptées et agréables.</p>
  <br><br>
  <h3>Cookies publicitaires et de réseaux sociaux</h3>
  <p>Ces cookies recueillent des informations sur vos habitudes en matière de navigation afin d'adapter au mieux la publicité à vos centres d'intérêts. Ils sont également utilisés pour limiter le nombre de fois que vous voyez une même annonce ainsi que pour mesurer l'efficacité d'une campagne publicitaire. Ces cookies sont généralement placés par des réseaux publicitaires tiers. Ils gardent en mémoire les sites Web que vous visitez et partagent ces informations avec des tierces parties telles que les annonceurs. Par exemple, nous utilisons des sociétés tierces afin de vous fournir des publicités personnalisées lorsque vous visitez d'autres sites Web.</p>
  <br><br>
  <p>En outre, les réseaux sociaux tels que Facebook et Twitter peuvent utiliser des cookies pour voir si vous êtes connecté et si vous pouvez par conséquent « aimer » le contenu ou le transmettre à vos amis. En fonction de vos paramètres, ces tiers peuvent utiliser vos informations à leurs propres fins, telles que pour de la publicité. Si vous souhaitez en savoir plus à ce sujet, veuillez vous référer aux politiques de confidentialité de ces tierces parties.</p>
