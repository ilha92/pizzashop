<h4>COMMENT VOIR MON SOLDE DE POINTS ?</h4>
<p>Votre compte PizzaShop contient toutes les informations concernant votre programme de fidélité. Vous y trouverez votre solde de points, vos pizzas offertes et l'historique de vos points.</p>
<br><br>
<h4>COMMENT FONCTIONNENT LES POINTS EN ATTENTE DE VALIDATION ?</h4>
<p>Lorsque vous passez une commande éligible de 15€ ou plus, vous obtenez 10 points en attente de validation. Une fois votre commande validée, ces points seront crédités sur votre compte sous 48 heures maximum.</p>
<br><br>
<h4>À QUOI CORRESPONDENT LES 15€ MINIMUM DE COMMANDE POUR GAGNER DES POINTS ?</h4>
<p>C'est le montant total payé par le client. Les 15€ de commande comprennent les taxes, les frais de livraison et les réductions éventuelles applicables.</p>
<br><br>
<h4>J'AI PASSÉ UNE COMMANDE DE 15€ MINIMUM CHEZ MON PIZZASHOP HABITUEL, PUIS-JE GAGNER DES POINTS ?</h4>
<p>Si vous n'avez pas passé votre commande sur pizzashop.fr en vous connectant à votre compte, vous ne pouvez pas gagner de points.</p>
<br><br>
<h4>VAIS-JE GAGNER DES POINTS SUR LES COMMANDES PASSÉES AVEC UNE PIZZA OFFERTE ?</h4>
<p>Oui, si le montant total de la commande est de 15€ minimum.</p>
<br><br>
<h4>J'AI OUBLIÉ DE ME CONNECTER À MON COMPTE AVANT DE PASSER COMMANDE, PUIS-JE RÉCLAMER CES POINTS ?</h4>
<p>Vous pouvez réclamer ces points depuis votre e-mail de confirmation de commande, cliquez sur la bannière "Ajoutez vos points de fidélité à votre compte". Vous avez jusqu'à 30 jours maximum après la commande pour réclamer ces points. Attention, vous ne pourrez le faire qu'une seule fois tous les 30 jours.</p>
<br><br>
<h4>QUE SE PASSE-T-IL SI JE PASSE UNE COMMANDE ÉLIGIBLE ET QUE JE N'OBTIENS PAS MES POINTS ?</h4>
<p>Après avoir passé une commande de 15€ minimum, vos points seront en attente de validation pendant 48 heures. Si au bout de 48 heures, vos points ne sont toujours pas crédités, veuillez nous contacter en utilisant le formulaire de contact. (Catégorie : PizzaShop Fidélité)</p>
