    <h2>Objectif de la page</h2>
    <br>
<p>Cette page est destinée aux chercheurs en sécurité souhaitant signaler une ou plusieurs vulnérabilités de sécurité des applications. Elle vise uniquement les vulnérabilités de sécurité des applications.</p>
<br>
<h2>Processus de divulgation</h2>
<br>
<p>Les détails que vous fournirez via le formulaire seront soumis à ResponsibleDisclosure.com (dirigé par Synack, un tiers indépendant). Si vous signalez un problème qui relève du champ d'application du programme et qui représente un problème de sécurité valide, ResponsibleDisclosure.com validera votre découverte et vous serez autorisé à divulguer la vulnérabilité après l'émission d'un correctif. Ce processus est géré exclusivement par ResponsibleDisclosure.com par le biais de sa plate-forme. Par conséquent, vous devez accepter les conditions de service de ResponsibleDisclosure.com si vous souhaitez poursuivre. Toute question doit être adressée à ResponsibleDisclosure.com et gérée exclusivement par le portail en ligne ResponsibleDisclosure.com.</p>
<br><br>
<h2>Directives du programme de divulgation responsable</h2>
<br>
<ul>
    <li>Accepter les conditions de service de ResponsibleDisclosure.com</li>
    <li>Travailler directement avec ResponsibleDisclosure.com sur les signalements de vulnérabilités en toute bonne foi</li>
    <li>Fournir la description détaillée d'une preuve de concept pour décrire comment reproduire les vulnérabilités</li>
    <li>Ne pas réaliser de tests perturbateurs tels que DoS ou toute action susceptible de nuire à la confidentialité, à l'intégrité ou à la disponibilité des informations et des systèmes</li>
    <li>Ne pas entreprendre d'ingénierie sociale ou de phishing de clients ou d'employés</li>
    <li>Vous n'avez pas droit à une compensation et vous ne demanderez pas de compensation pour le temps passé, le matériel utilisé ou les vulnérabilités découvertes</li>
    <li>Comprendre la complexité du processus d'évaluation : L'adjudication d'une vulnérabilité se fait en tenant compte de la portée du programme ainsi que des facteurs de réduction qui peuvent supprimer ou réduire les risques spécifiques à des niveaux acceptables. Les décisions se prennent de manière réfléchie et sont définitives</li>
</ul>
